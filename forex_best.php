                 <p><strong>The Role of Price Action Trading in Forex Strategies</strong></p>

<p>To what extent fundamentals are used varies from trader to trader. At the same time, the best FX strategies invariably utilize action. This is also known as technical analysis. When it comes to technical currency trading strategies, there are two main styles: trend following, and counter-trend trading. Both of these FX trading strategies try to profit by recognising and exploiting price patterns. When it comes to price patterns, the most important concepts include ones such as support and resistance. Put simply, these terms represent the tendency of a market to bounce back from previous lows and highs. Support is the market's tendency to rise from a previously established low. Resistance is the market's tendency to fall from a previously established high. This occurs because market participants tend to judge subsequent prices against recent highs and lows.</p>

<p>What happens when the market approaches recent lows? Put simply, buyers will be attracted to what they regard as cheap. What happens when the market approaches recent highs? Sellers will be attracted to what they view as either expensive, or a good place to lock in a profit. Therefore, recent highs and lows are the yardstick by which current prices are evaluated. There is also a self-fulfilling aspect to support and resistance levels. This happens because market participants anticipate certain price action at these points and act accordingly. As a result, their actions can contribute to the market behaving as they had expected.</p>


<p><strong>However, it's worth noting these three things:</strong></p>

<p>Support and resistance levels do not present ironclad rules, they are simply a common consequence of the natural behaviour of market participants.</p>

<p>Trend-following systems aim to profit from the times when support and resistance levels break down.</p>

<p>Counter-trending styles of trading are the opposite of trend following—they aim to sell when there's a new high, and buy when there's a new low.</p>

<p><strong>Trend-Following Forex Strategies</strong></p>

<p>Sometimes a market breaks out of a range, moving below the support or above the resistance to start a trend. How does this happen? When support breaks down and a market moves to new lows, buyers begin to hold off. This is because buyers are constantly noticing cheaper prices being established and want to wait for a bottom to be reached. At the same time, there will be traders who are selling in panic or simply being forced out of their positions. The trend continues until the selling is depleted and belief starts to return to buyers when it is established that the prices will not decline further. Trend-following strategies encourage traders to buy on the markets once they have broken through resistance and sell markets, and when they have fallen through support levels. In addition, trends can be dramatic and prolonged, too. Because of the magnitude of moves involved, this type of system has the potential to be the most successful Forex trading strategy. Trend-following systems use indicators to inform traders when a new trend may have begun, but there's no sure-fire way to know of course.</p>

